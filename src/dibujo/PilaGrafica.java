    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        try{
            if(pila.getNodos().size()<8){
                pila.push(Integer.parseInt(t.getText()));
                pd.repaint();
                repaint();
                t.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "Maximo se mostraran 8 elementos.");
            }
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Error digite un Valor Valido");
        }
 
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        if(pila.getNodos().size()>0){
            pila.pop();
            pd.repaint();
            repaint();
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

